package edu.csu.movies;


public class MovieEntity{
	
	private String movie_id;
    private String movie_title;
    private String movie_date;//上映日期
    private String movie_pic;
    private String movie_time;//时长
    private String movie_director;
    private String movie_fileaddress;
    private String movie_classes;
    private String movie_introduction;
    private String movie_actor;
    private String movie_score;
    private String movie_zone;
    
	public String getMovie_id() {
		return movie_id;
	}
	public void setMovie_id(String movie_id) {
		this.movie_id = movie_id;
	}
	public String getMovie_title() {
		return movie_title;
	}
	public void setMovie_title(String movie_title) {
		this.movie_title = movie_title;
	}
	public String getMovie_date() {
		return movie_date;
	}
	public void setMovie_date(String movie_date) {
		this.movie_date = movie_date;
	}
	public String getMovie_pic() {
		return movie_pic;
	}
	public void setMovie_pic(String movie_pic) {
		this.movie_pic = movie_pic;
	}
	public String getMovie_time() {
		return movie_time;
	}
	public void setMovie_time(String movie_time) {
		this.movie_time = movie_time;
	}
	public String getMovie_director() {
		return movie_director;
	}
	public void setMovie_director(String movie_director) {
		this.movie_director = movie_director;
	}
	public String getMovie_fileaddress() {
		return movie_fileaddress;
	}
	public void setMovie_fileaddress(String movie_fileaddress) {
		this.movie_fileaddress = movie_fileaddress;
	}
	public String getMovie_classes() {
		return movie_classes;
	}
	public void setMovie_classes(String movie_classes) {
		this.movie_classes = movie_classes;
	}
	public String getMovie_introduction() {
		return movie_introduction;
	}
	public void setMovie_introduction(String movie_introduction) {
		this.movie_introduction = movie_introduction;
	}
	public String getMovie_actor() {
		return movie_actor;
	}
	public void setMovie_actor(String movie_actor) {
		this.movie_actor = movie_actor;
	}
	public String getMovie_score() {
		return movie_score;
	}
	public void setMovie_score(String movie_score) {
		this.movie_score = movie_score;
	}
	public String getMovie_zone() {
		return movie_zone;
	}
	public void setMovie_zone(String movie_zone) {
		this.movie_zone = movie_zone;
	}

	
    
}
